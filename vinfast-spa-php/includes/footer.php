        const isDark = html.classList.contains('dark');
        if (isDark) {
            html.classList.remove('dark');
            document.body.classList.add('light-mode');
            localStorage.setItem('theme', 'light');
        } else {
            html.classList.add('dark');
            document.body.classList.remove('light-mode');
            localStorage.setItem('theme', 'dark');
        }
    });

    // Mobile Menu Toggle
    const mobileMenuBtn = document.getElementById('mobileMenuBtn');
    const mobileDrawer = document.getElementById('mobileDrawer');
    let menuOpen = false;

    mobileMenuBtn.addEventListener('click', () => {
        menuOpen = !menuOpen;
        mobileDrawer.style.display = menuOpen ? 'flex' : 'none';
    });

    // Global Search (AJAX)
    const searchInput = document.getElementById('globalSearch');
    const searchResults = document.getElementById('searchResults');

    searchInput.addEventListener('input', function() {
        const q = this.value.trim();
        if (q.length < 2) {
            searchResults.classList.remove('active');
            return;
        }
        fetch('api-search.php?q=' + encodeURIComponent(q))
            .then(r => r.json())
            .then(data => {
                if (data.products.length === 0 && data.blogs.length === 0) {
                    searchResults.innerHTML = '<div class="search-no-result">Không tìm thấy kết quả</div>';
                } else {
                    let html = '';
                    if (data.products.length > 0) {
                        data.products.forEach(p => {
                            html += `
                            <a href="${p.url}" class="search-result-item">
                                <div class="search-result-info">
                                    <div class="search-result-name">${p.title}</div>
                                    <div class="search-result-price">${p.car_name}</div>
                                </div>
                            </a>`;
                        });
                    }
                    if (data.blogs.length > 0) {
                        data.blogs.forEach(b => {
                            html += `
                            <a href="${b.url}" class="search-result-item">
                                <div class="search-result-info">
                                    <div class="search-result-name">${b.title}</div>
                                    <div class="search-result-price">Cẩm nang</div>
                                </div>
                            </a>`;
                        });
                    }
                    searchResults.innerHTML = html;
                }
                searchResults.classList.add('active');
            });
    });

    // Close search on click outside
    document.addEventListener('click', (e) => {
        if (!e.target.closest('.search-container')) {
            searchResults.classList.remove('active');
        }
    });
